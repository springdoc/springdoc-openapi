package test.org.springdoc.api.v30.app2.controller;

import test.org.springdoc.api.v30.app2.entities.Post;
import test.org.springdoc.api.v30.app2.service.PostService;

import org.springframework.data.domain.Pageable;
import org.springframework.hateoas.EntityModel;
import org.springframework.hateoas.PagedModel;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RestController;

/**
 * @author Davide Pedone
 * 2020
 */
@RestController
public class PostController {

	private final PostService postService;

	public PostController(PostService postService) {
		this.postService = postService;
	}

	@GetMapping
	public ResponseEntity<PagedModel<EntityModel<Post>>> getAll(Pageable pageable) {
		return new ResponseEntity<>(postService.getAll(pageable), HttpStatus.OK);
	}
}
